export default async function ServicePageWrapper({ slug }: { slug: string }) {
  const raw = await fs.readFile(path.join(process.cwd(), 'data', 'content.json'), 'utf-8');
  const phone: string = JSON.parse(raw)?.contacts?.phone ?? '+7 (3952) 69-70-02';
  const entry = CONFIGS[slug];
  if (!entry) return null;
  const cfg: ServicePageConfig = { ...entry, phone };

  return (
    <>
      <script
        type="application/ld+json"
        dangerouslySetInnerHTML={{
          __html: JSON.stringify({
            '@context': 'https://schema.org',
            '@type': 'Service',
            name: cfg.schemaName,
            provider: { '@type': 'LocalBusiness', name: 'Федерация Холода' },
            areaServed: 'Иркутск',
            url: cfg.schemaUrl,
          }),
        }}
      />
      <div className="bg-slate-900 px-4 sm:px-6 lg:px-8 pt-6">
        <div className="max-w-7xl mx-auto">
          <Breadcrumbs
            items={[
              { label: 'Услуги', href: '/services' },
              { label: entry.breadcrumbLabel, href: `/services/${slug}` },
            ]}
          />
        </div>
      </div>
      <ServicePageTemplate cfg={cfg} />
    </>
  );
}
